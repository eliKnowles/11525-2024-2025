package org.firstinspires.ftc.teamcode.code.limelight;

public class Sample {
    public final double tx;
    public final double ty;

    public Sample(double tx, double ty) {
        this.tx = tx;
        this.ty = ty;
    }

    public double getTx() {
        return tx;
    }

    public double getTy() {
        return ty;
    }
}
